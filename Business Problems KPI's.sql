Blinkit Business Problems & Solutions
KPI’s

Total Sales – The overall revenue generated from all items sold.

Average Sales – The average revenue per sale.

Number of Items – The total count of different items sold.

Average Rating – The average customer rating for items sold.

Granular Analysis

Total Sales by Fat Content – Analyze the impact of fat content (Low Fat, Regular) on total sales.

Total Sales by Item Type – Identify which product categories contribute most to sales.

Fat Content by Outlet for Total Sales – Compare total sales across outlets segmented by fat content.

Total Sales by Outlet Establishment – Evaluate how the age or type of outlet influences sales.

Charts / Advanced KPIs

Percentage of Sales by Outlet Size – Correlation between outlet size (Small, Medium, Large) and sales.

Sales by Outlet Location – Geographic distribution of sales across different locations.

All Metrics by Outlet Type – Compare all KPIs (Total Sales, Avg Sales, Number of Items, Avg Rating, Visibility) across outlet types
